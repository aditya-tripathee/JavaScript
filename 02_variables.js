// Variables in JS 
// Variables are containers for data

// for example :-- 
var age = 22;
console.log(age);

myName = "Adii";
console.log(myName);

// Variables rules :--
// 1. Variables names are case sensitive , "a" and "A" is different.
// for example :-- 
fullname = "Aditya";
FullName = "adii";
console.log(fullname);
console.log(FullName);

// 2. Only letters , digits , underscore(_) and $ is allowed.
my_address = "Gorakhpur";
console.log(my_address);

$city = "delhi";
console.log($city);

// 3. Only letters , underscore(_) or $ is allowed for 1st character.
_rollnumber = 21;
console.log(_rollnumber);
$country = "india";
console.log($country);

// 4. Reserved words cannot be used for variables names.
//  console = "aditya";
//  console.log(console);
//  it is error because console is reserved words.




//Varaibles Naming convention 

// 1. camelCase :--
 myGrade = "A+";
 console.log(myGrade);
 

// 2. snakecase
 my_city = "gorakhpur";
 console.log(my_city);
  
// 3. Pascal Case 
 MyAddress = "Maharajganj";
 console.log(MyAddress);
 
  
// Variables Keyword:--

// var :-- Varaibles can be re-declared or updated. A global scope vaariable.
 var myName = "ADitya";
 console.log(myName);
 
// let :-- Variables cannot be re-declared but can be updated. A block scope variable.
let mycity  = "nautanwa";
console.log(mycity);

mycity = "sonauli";
console.log(mycity);

// const :-- Variables caannot be re-declared or updated. A block scope variable.
const pi = 3.14;
console.log(pi);

